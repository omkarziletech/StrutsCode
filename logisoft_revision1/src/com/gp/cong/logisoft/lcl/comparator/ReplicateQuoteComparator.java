package com.gp.cong.logisoft.lcl.comparator;

import com.gp.cong.logisoft.domain.lcl.LclBooking;
import com.gp.cong.logisoft.domain.lcl.LclQuote;
import java.util.Comparator;

/**
 *
 * @author Administrator
 */
public class ReplicateQuoteComparator implements Comparator<Object> {

    public int compare(Object obj1, Object obj2) {

        Long id1 = null;
        if (obj1 instanceof LclQuote) {
            id1 = ((LclQuote) obj1).getLclFileNumber().getId();
        } else if (obj1 instanceof LclBooking) {
            id1 = ((LclBooking) obj1).getLclFileNumber().getId();
        }
        Long id2 = null;
        if (obj2 instanceof LclQuote) {
            id2 = ((LclQuote) obj2).getLclFileNumber().getId();
        } else if (obj2 instanceof LclBooking) {
            id2 = ((LclBooking) obj2).getLclFileNumber().getId();
        }

        return id2.compareTo(id1);

    }
}
