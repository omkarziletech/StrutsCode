package com.gp.cong.logisoft.lcl.comparator;

import com.logiware.accounting.model.ManifestModel;
import java.util.Comparator;

/**
 *
 * @author Administrator
 */
public class LclManifestModelComparator implements Comparator<ManifestModel> {

    public int compare(ManifestModel mm1, ManifestModel mm2) {
        if (mm1 == null || mm1.getFileId() == null || mm1.getFileId()==0) {
            return -1;
        }
        if (mm2 == null || mm2.getFileId() == null && mm2.getFileId()==0) {
            return 1;
        }
        return mm1.getFileId().compareTo(mm2.getFileId());
    }
}
