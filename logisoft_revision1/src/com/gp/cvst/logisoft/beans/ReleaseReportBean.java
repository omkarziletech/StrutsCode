
package com.gp.cvst.logisoft.beans;

/**
 *
 * @author NambuRajasekar
 */
public class ReleaseReportBean {
    
    private String unLocation;
    private Integer totalDr;
    private Integer pieceCount;
    private String weight;
    private String cube;

    public String getUnLocation() {
        return unLocation;
    }

    public void setUnLocation(String unLocation) {
        this.unLocation = unLocation;
    }

    public Integer getTotalDr() {
        return totalDr;
    }

    public void setTotalDr(Integer totalDr) {
        this.totalDr = totalDr;
    }

    public Integer getPieceCount() {
        return pieceCount;
    }

    public void setPieceCount(Integer pieceCount) {
        this.pieceCount = pieceCount;
    }

    public String getWeight() {
        return weight;
    }

    public void setWeight(String weight) {
        this.weight = weight;
    }

    public String getCube() {
        return cube;
    }

    public void setCube(String cube) {
        this.cube = cube;
    }

    
}
