package com.logiware.form;

import com.gp.cong.hibernate.FclBlContainerDtls;
import com.gp.cong.logisoft.hibernate.dao.GenericCodeDAO;
import com.gp.cong.logisoft.util.DBUtil;
import java.util.List;
import org.apache.struts.action.ActionForm;

/**
 *
 * @author Shanmugam R
 */
public class FclBlContainerForm extends ActionForm {
    private FclBlContainerDtls fclBlContainerDtls;
    private Integer trailerNoId;
    private String methodName;
    private String bol;
    private String breakBulk;
    private String readyToPost;
    private List unitTypeList;
    private String sizeLegend;
    


    public String getMethodName() {
        return methodName;
    }

    public void setMethodName(String methodName) {
        this.methodName = methodName;
    }
    public FclBlContainerForm() {
        this.fclBlContainerDtls = new FclBlContainerDtls();
    }

    public FclBlContainerDtls getFclBlContainerDtls() {
        return fclBlContainerDtls;
    }

    public void setFclBlContainerDtls(FclBlContainerDtls fclBlContainerDtls) {
        this.fclBlContainerDtls = fclBlContainerDtls;
    }

    public Integer getTrailerNoId() {
        return this.getFclBlContainerDtls().getTrailerNoId();
    }

    public void setTrailerNoId(Integer trailerNoId) {
        this.trailerNoId = trailerNoId;
    }

    public List getUnitTypeList()throws Exception {
        return new DBUtil().getUnitFCLUnitypeTest(new Integer(38), "yes", "Select Unit code", null,getBol());
    }

    public void setUnitTypeList(List unitTypeList) {
        this.unitTypeList = unitTypeList;
    }

    public String getBol() {
        return bol;
    }

    public void setBol(String bol) {
        this.bol = bol;
    }

    public String getBreakBulk() {
        return breakBulk;
    }

    public void setBreakBulk(String breakBulk) {
        this.breakBulk = breakBulk;
    }

    public String getReadyToPost() {
        return readyToPost;
    }

    public void setReadyToPost(String readyToPost) {
        this.readyToPost = readyToPost;
    }

    public String getSizeLegend() {
        return null != fclBlContainerDtls.getSizeLegend()?""+fclBlContainerDtls.getSizeLegend().getId():"0";
    }

    public void setSizeLegend(String sizeLegend)throws Exception {
        if(null != sizeLegend){
            fclBlContainerDtls.setSizeLegend(new GenericCodeDAO().findById(Integer.parseInt(sizeLegend)));
        }
    }

    
    
}
